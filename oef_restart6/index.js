// print getallen entussen 50 en 100

for (let i=50; i<= 100;i++){
    console.log(i);
}
//For-loop Initiatie:
/*
for: Dit is de start van een for-loop.
Een for-loop wordt gebruikt om een blok code herhaaldelijk uit te voeren, gebaseerd op een bepaalde voorwaarde.
let i = 50: Hier wordt de variabele i gedeclareerd en geÃ¯nitialiseerd met de waarde 50.
Dit is het startpunt van de loop. i fungeert als de teller die we gaan gebruiken.
i <= 100: Dit is de conditie die bepaalt hoe lang de loop moet doorgaan.
De loop blijft draaien zolang i kleiner dan of gelijk aan 100 is. Zodra i groter wordt dan 100, stopt de loop.
i++: Dit betekent dat i met 1 toeneemt bij elke iteratie van de loop.
Dit is essentieel om ervoor te zorgen dat de loop uiteindelijk stopt; anders zou de loop oneindig doorgaan.
 */